#include<iostream>
#include<cmath>
using namespace std;

int main(){
    cout<<"Maximum Value: "<<max(5,100)<<endl;
    cout<<"Minimum Value: "<<min(5,100)<<endl;

    cout<<sqrt(6400)<<endl;
    cout<<round(12341314142524.029809473278327021)<<endl;
    cout<<log(2)<<endl;

    // returns the absolute value of x:
    cout<<abs(-9.673)<<endl;
    // returns the value of x rounded up to 
    //its nearest integer
    cout<<ceil(44.1)<<endl;
    // returns the value of x to the power of y
    cout<<pow(2, 3)<<endl;
    
    return 0;
}